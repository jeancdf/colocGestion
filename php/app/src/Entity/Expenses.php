<?php

namespace App\Entity;

class Expenses extends BaseEntity
{
    private int $id;
    private int $flatsharing_id;
    private string $user_id;
    private int $amount;
    private string $date;




    /**
     * Get the value of id
     */ 
    public function getId()
    {
        return $this->id;
    }

    /**
     * Set the value of id
     *
     * @return  self
     */ 
    public function setId($id)
    {
        $this->id = $id;

        return $this;
    }

    /**
     * Get the value of flatsharing_id
     */ 
    public function getFlatsharing_id()
    {
        return $this->flatsharing_id;
    }

    /**
     * Set the value of flatsharing_id
     *
     * @return  self
     */ 
    public function setFlatsharing_id($flatsharing_id)
    {
        $this->flatsharing_id = $flatsharing_id;

        return $this;
    }

    /**
     * Get the value of user_id
     */ 
    public function getUser_id()
    {
        return $this->user_id;
    }

    /**
     * Set the value of user_id
     *
     * @return  self
     */ 
    public function setUser_id($user_id)
    {
        $this->user_id = $user_id;

        return $this;
    }

    /**
     * Get the value of amount
     */ 
    public function getAmount()
    {
        return $this->amount;
    }

    /**
     * Set the value of amount
     *
     * @return  self
     */ 
    public function setAmount($amount)
    {
        $this->amount = $amount;

        return $this;
    }

    /**
     * Get the value of date
     */ 
    public function getDate()
    {
        return $this->date;
    }

    /**
     * Set the value of date
     *
     * @return  self
     */ 
    public function setDate($date)
    {
        $this->date = $date;

        return $this;
    }
}
